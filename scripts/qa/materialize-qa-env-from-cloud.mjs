#!/usr/bin/env node
/**
 * Writes `.env.qa.local` from Cloud Agent / CI injected env vars so
 * `npm run qa:*` works without manual copy-paste from `.env.qa.example`.
 *
 * Usage (cloud agents, before qa runners):
 *   node scripts/qa/materialize-qa-env-from-cloud.mjs
 *   npm run qa:materialize-env   # then qa:cache / qa:chunks / qa:google-signup
 *
 * Resolves QA_PUBLIC_PROFILE_UID from QA_TEST_EMAIL when unset (Firebase Auth
 * REST sign-in). Defaults QA_APPCHECK_DEBUG_TOKEN to the registered localhost
 * debug UUID documented in AGENTS.md / docs/TESTING.md.
 */

import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, '../../.env.qa.local');

const DEFAULT_APPCHECK = '38422efd-029f-45b4-b028-7cf7fcaeeffc';

/**
 * @param {string} email
 * @param {string} password
 * @returns {Promise<string>}
 */
async function resolveUidFromTestAccount(email, password) {
  const apiKey = process.env.VITE_FIREBASE_API_KEY?.trim();
  if (!apiKey) throw new Error('VITE_FIREBASE_API_KEY required to resolve QA_PUBLIC_PROFILE_UID');

  const res = await fetch(
    `https://identitytoolkit.googleapis.com/v1/accounts:signInWithPassword?key=${apiKey}`,
    {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Referer: 'https://www.setlistpickem.com/',
        Origin: 'https://www.setlistpickem.com',
      },
      body: JSON.stringify({ email, password, returnSecureToken: true }),
    },
  );
  const json = await res.json();
  if (!json.localId) {
    throw new Error(json.error?.message || 'Could not sign in QA test account to resolve UID');
  }
  return json.localId;
}

async function main() {
  const email = process.env.QA_TEST_EMAIL?.trim();
  const password = process.env.QA_TEST_PASSWORD?.trim();
  if (!email || !password) {
    console.error(
      '[qa:materialize-env] QA_TEST_EMAIL and QA_TEST_PASSWORD must be set (Cloud Agent secrets).',
    );
    process.exit(1);
  }

  let uid = process.env.QA_PUBLIC_PROFILE_UID?.trim();
  if (!uid) {
    uid = await resolveUidFromTestAccount(email, password);
    console.log(`[qa:materialize-env] resolved QA_PUBLIC_PROFILE_UID=${uid} from QA_TEST_EMAIL`);
  }

  const appCheck = process.env.QA_APPCHECK_DEBUG_TOKEN?.trim() || DEFAULT_APPCHECK;
  const googleEmail = process.env.QA_GOOGLE_TEST_EMAIL?.trim();
  const googlePassword = process.env.QA_GOOGLE_TEST_PASSWORD?.trim();

  const lines = [
    '# Auto-generated by scripts/qa/materialize-qa-env-from-cloud.mjs',
    `QA_PUBLIC_PROFILE_UID=${uid}`,
    `QA_APPCHECK_DEBUG_TOKEN=${appCheck}`,
    `QA_TEST_EMAIL=${email}`,
    `QA_TEST_PASSWORD=${password}`,
  ];
  if (googleEmail && googlePassword) {
    lines.push(`QA_GOOGLE_TEST_EMAIL=${googleEmail}`);
    lines.push(`QA_GOOGLE_TEST_PASSWORD=${googlePassword}`);
  }

  writeFileSync(OUT, `${lines.join('\n')}\n`, 'utf8');
  console.log(`[qa:materialize-env] wrote ${OUT}`);
  if (!googleEmail) {
    console.log(
      '[qa:materialize-env] QA_GOOGLE_TEST_* not set — qa:google-signup OAuth phase will SKIP.',
    );
  }
}

main().catch((err) => {
  console.error('[qa:materialize-env] failed:', err.message || err);
  process.exit(1);
});
